#include "libft.h"

/**
 * Adds the element â€™newâ€™ at the end of the list.
*/
void	ft_lstadd_back(t_list **lst, t_list *new)
{
	if (*lst == NULL)
	{
		*lst = new;
		return ;
	}
	ft_lstlast(*lst)->next = new;
}
